// add()
function add(num1, num2){
	return num1+num2;
}
// subtract

function subtract(num1, num2){
	return num1- num2;
}

  // multiply()

function multiply(num1, num2){
	return num1*num2;
} 

// divide()

function divide(num1, num2){
	return num1/num2;
}

 // square()

function square(num1){
	return num1*num1;
}

// squareRoot()
function squareRoot(num1){
	return Math.sqrt(num1);
}
 
